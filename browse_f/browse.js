function displayData(fundData){
    document.getElementById("data").innerText ="";
    fundData.map(function(elem,index){
  var div = document.createElement("div");
  div.setAttribute("id","parentBox");
  var image = document.createElement("img");
   image.setAttribute("id","avtar")
   image.setAttribute("src", elem.img)
   image.setAttribute("alt",index);
  var h4 = document.createElement("h4");
  h4.innerText = elem.heading;
//   h4.setAttribute("id", "h4");
  var smallDiv = document.createElement("div");
    smallDiv.setAttribute("id", "logo")
  var logo = document.createElement("img");
    logo.setAttribute("id","imgLogo")
   logo.setAttribute("src", elem.logo);
   logo.setAttribute("alt", index);

   var pName = document.createElement("p");
   pName.innerText = elem.pName;

    var amountDiv = document.createElement("div");
    amountDiv.setAttribute("id","amount");
    var amount = document.createElement("h3")
     amount.innerText = elem.amount;
    
     var raised = document.createElement("p");
     raised .innerText = "raised";
     raised.setAttribute("id", "raised");

     var hr = document.createElement("hr");
     hr.setAttribute("id", "hr");
     
     var pLast = document.createElement("p");
     pLast.innerText = "Last donation an hours ago";
     pLast.setAttribute("id", "pLast");
  
     var newDiv = document.createElement("div");
     newDiv.setAttribute("id", "newDiv");

      var dayDiv = document.createElement("div");
      dayDiv.setAttribute("id","day")
      var day = document.createElement("h3");
      day.innerText = elem.days;

       var dayLeft = document.createElement("p");
       dayLeft.innerText ="Day Left";
       
       var supportersDiv = document.createElement("div");
       supportersDiv.setAttribute("id", "supporters");
       var supporters = document.createElement("h3");
       supporters.innerText = elem.support;

        var pSupporters = document.createElement("p")
        pSupporters.innerText = "Supporters"

        newDiv.append(dayDiv,supportersDiv);
        supportersDiv.append(supporters, pSupporters);
        dayDiv.append(day,dayLeft);
        amountDiv.append(amount, raised);
        smallDiv.append(logo,pName);
         
        div.append(image, h4, smallDiv, amountDiv, hr, pLast, newDiv);

        document.getElementById("data").append(div);

    });
}



